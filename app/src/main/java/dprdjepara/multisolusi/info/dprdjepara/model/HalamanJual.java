package dprdjepara.multisolusi.info.dprdjepara.model;

public class HalamanJual {
	private Integer jumlahbarisjual;
	
	public HalamanJual() {
		super();
	}

	public HalamanJual(Integer jumlahbarisjual) {
		super();
		this.jumlahbarisjual = jumlahbarisjual;
	}

	public Integer getJumlahbarisjual() {
		return jumlahbarisjual;
	}

	public void setJumlahbarisjual(Integer jumlahbarisjual) {
		this.jumlahbarisjual = jumlahbarisjual;
	}

}