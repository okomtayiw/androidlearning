package dprdjepara.multisolusi.info.dprdjepara.model;

public class HalamanRapat {
	private Integer jumlahbarisrapat;
	
	public HalamanRapat() {
		super();
	}

	public HalamanRapat(Integer jumlahbarisrapat) {
		super();
		this.jumlahbarisrapat = jumlahbarisrapat;
	}

	public Integer getJumlahbarisrapat() {
		return jumlahbarisrapat;
	}

	public void setJumlahbarisrapat(Integer jumlahbarisrapat) {
		this.jumlahbarisrapat = jumlahbarisrapat;
	}

}